export function getGlobalReply(text: string): string {
  const lower = text.toLowerCase();
  for (const key of Object.keys(globalReplies)) {
    if (lower.includes(key)) return globalReplies[key];
  }
  return "Анализирую контекст канала... Дай чуть больше деталей: это про конкретный пост или про канал в целом?";
}

